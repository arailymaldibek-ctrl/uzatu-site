const ADMIN_PASSWORD = "uzatu2026";

function getLang() {
  return localStorage.getItem('uzatu_lang') ||
    (navigator.language?.startsWith('kk') ? 'kz' : 'ru');
}
function setLang(l) { localStorage.setItem('uzatu_lang', l); }
function t(key) { return T[getLang()][key] || T['ru'][key] || key; }

function applyLang() {
  const l = getLang();
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (el.placeholder !== undefined && el.dataset.i18nPh) {
      el.placeholder = t(el.dataset.i18nPh);
    } else {
      el.textContent = t(key);
    }
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPh);
  });
  const btn = document.getElementById('lang-btn');
  if (btn) btn.textContent = l === 'ru' ? 'KZ' : 'RU';
  document.documentElement.lang = l === 'kz' ? 'kk' : 'ru';
}

function toggleLang() {
  setLang(getLang() === 'ru' ? 'kz' : 'ru');
  location.reload();
}
